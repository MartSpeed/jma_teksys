package keywords_expression;

public class Keyword_Expressions {

	public static void main(String[] args) {
		// Expressions
		// building blocks of java program
		// a mile is equal to 1.609344 kilometers
		double kilometers = (100 * 1.609344);
		System.out.println(kilometers);
		// within the brackets is the expression
		int highScore = 50;
		if (highScore == 50) {
			System.out.println("The components within in the brackets is also an expression");
		}
	}

}
